/**
 * Represents the gamepad axes used in the input system.
 *
 * @export
 * @enum {number}
 */
export enum GamepadAxis {
    Axis0 = 0,
    Axis1 = 1,
    Axis2 = 2,
    Axis3 = 3,
    Axis4 = 4,
    Axis5 = 5,

    LeftStickX = Axis0,
    LeftStickY = Axis1,
    RightStickX = Axis2,
    RightStickY = Axis3,
    LeftTrigger = Axis4,
    RightTrigger = Axis5,
}
