/**
 * Represents the mouse buttons used in the input system.
 *
 * @export
 * @enum {number}
 */
export enum MouseButton {
    Button0 = 0,
    Button1 = 1,
    Button2 = 2,
    Button3 = 3,
    Button4 = 4,

    Left = Button0,
    Center = Button1,
    Right = Button2,
    Back = Button3,
    Forward = Button4,
}
